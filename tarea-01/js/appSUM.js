
// Funcion que pide 2 numeros al usuario y los suma entre si 
function SUMA() {

alert('PROGRAMA QUE INGRESA 2 NUMEROS Y LOS SUMA ENTRE ELLOS');    

let a = prompt("Introduce el valor de a:");
let b = prompt("Introduce el valor de b:");



// Realizar la suma
let resultado = parseInt(a) + parseInt(b);


alert(`El resultado de la suma es: ${resultado}`);

}

SUMA();
